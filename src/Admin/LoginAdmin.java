    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String us = txtUsuario.getText(); // usuario
    String contra = txtContraseña.getText(); // contraseña (puedes omitir este campo si no es necesario)

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conne = DriverManager.getConnection("jdbc:mysql://localhost/proyectoe", "root", "1063616908");
        Statement st = conne.createStatement();
        
        // Modificar la consulta para traer solo el nombre y el tipousuarioId
        String sql = "SELECT nombreUsuario, tipousuarioId FROM usuario WHERE nombreUsuario = '" + us + "'";
        ResultSet rs = st.executeQuery(sql);

        if (rs.next()) {
            String nombre = rs.getString("nombreUsuario");
            int tipousuarioId = rs.getInt("tipousuarioId");

            // Aquí puedes manejar lo que sucede según el tipo de usuario
            switch (tipousuarioId) {
                case 1:
                    // Es un administrador
                    // Lógica para administrador
                    Admin_Principal Epi = new Admin_Principal();
                    Epi.setVisible(true);
                    Epi.pack();
                    Epi.setLocationRelativeTo(null);
                    Epi.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    dispose();
                    break;
                case 2:
                    // Es un estudiante
                    JOptionPane.showMessageDialog(null, "usuario, contraseña incorrectos");
                    break;
                case 3:
                    // Es un profesor
                    JOptionPane.showMessageDialog(null,"usuario, contraseña incorrectos");
                    break;
                default:
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Usuario no encontrado");
        }
    } catch (HeadlessException | ClassNotFoundException | SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    }

    }//GEN-LAST:event_jButton1ActionPerformed

