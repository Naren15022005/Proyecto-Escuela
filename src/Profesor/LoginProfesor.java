    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   String us = txtUsuario.getText(); // Usuario
        String contra = new String(txtContraseña.getPassword()); // Contraseña

        Connection conne = null;
        PreparedStatement ps = null;
        ResultSet rs = null;

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conne = DriverManager.getConnection("jdbc:mysql://localhost/proyectoe", "root", "1063616908");
            String sql = "SELECT nombreUsuario, tipousuarioId FROM usuario WHERE nombreUsuario = ? AND contraseña = ?";
            ps = conne.prepareStatement(sql);
            ps.setString(1, us);
            ps.setString(2, contra);
            rs = ps.executeQuery();

            if (rs.next()) {
                int tipousuarioId = rs.getInt("tipousuarioId");

                if (tipousuarioId == 3) {
                    ProfesorPrincipal Epi = new ProfesorPrincipal();
                    Epi.setVisible(true);
                    Epi.pack();
                    Epi.setLocationRelativeTo(null);
                    Epi.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Tipo de usuario no reconocido");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario o contraseña incorrectos");
            }
        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
       // Imprime detalles del error en la consola
        } finally {
            try {
                if (rs != null) rs.close();
                if (ps != null) ps.close();
                if (conne != null) conne.close();
            } catch (SQLException e) {
            }
        }
        
    


   
                        



    }//GEN-LAST:event_jButton1ActionPerformed

