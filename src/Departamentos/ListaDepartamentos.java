private void mostrar() {
   DefaultTableModel modelo = new DefaultTableModel();
modelo.addColumn("id");
modelo.addColumn("nombre");

jTable1.setModel(modelo);

// Consulta SQL para obtener el nombre del departamento
String sql = "SELECT id, nombre FROM departamento";

String datos[] = new String[2];

Statement at;

try {
    // Crea un statement a partir de la conexi√≥n
    at = conectar.createStatement();
    ResultSet rs = at.executeQuery(sql);

    while (rs.next()) {
        datos[0] = rs.getString("id");  // ID del departamento
        datos[1] = rs.getString("nombre");  // Nombre del departamento
     
        modelo.addRow(datos);
    }
} catch (SQLException e) {
    Logger.getLogger(ListaCalificaciones.class.getName()).log(Level.SEVERE, null, e);
}
}
