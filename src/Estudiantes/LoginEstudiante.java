    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   String us = txtUsuario.getText(); // usuario
   String contra = txtContraseña.getText(); // contraseña

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conne = DriverManager.getConnection("jdbc:mysql://localhost/proyectoe", "root", "1063616908");
        Statement st = conne.createStatement();
        
        // Modificar la consulta para traer el id del estudiante además del nombre y el tipousuarioId
        String sql = "SELECT id, nombreUsuario, tipousuarioId FROM usuario WHERE nombreUsuario = ? AND contraseña = ?";
        PreparedStatement pst = conne.prepareStatement(sql);
        pst.setString(1, us);
        pst.setString(2, contra);
        ResultSet rs = pst.executeQuery();


       if (rs.next()) {
    int studentId = rs.getInt("id"); // Capturamos el ID del estudiante
    String nombre = rs.getString("nombreUsuario");
    int tipousuarioId = rs.getInt("tipousuarioId");

    switch (tipousuarioId) {
        case 2:
            // Guardar el ID del estudiante en la sesión
            SesionEstudiante.getInstance().setEstudianteId(studentId);
            SesionCursos.getInstance().setCursoId(cursoId);

            // Si es un estudiante
            EstudiantePrincipal Ep = new EstudiantePrincipal();
            Ep.setVisible(true);
            Ep.pack();
            Ep.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            dispose(); // Cerrar ventana de login
            break;
        default:
            JOptionPane.showMessageDialog(null, "usuario, contraseña incorrectos");
            break;
    }
} else {
    JOptionPane.showMessageDialog(null, "Usuario no encontrado");
}

    } catch (HeadlessException | ClassNotFoundException | SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

